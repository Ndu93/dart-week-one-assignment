//loops
void main() {
  // For loop printing numbers from 1 to 10
  print("For Loop (1 to 10):");
  for (int i = 1; i <= 10; i++) {
    print(i);
  }

  print("\n"); // Adding a blank line for readability

  print("While Loop (10 to 1):");
  int num = 10;
  while (num >= 1) {
    print(num);
    num--;
  }
}
